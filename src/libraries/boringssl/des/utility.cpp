int des_config_init(size_t cache_size,
                    config_t *&config) {

    des_config_t *des_config = (des_config_t *)config;

    // configuration
    int block_size = 8;
    int block_count = SWAN_TXT_INPUT_SIZE / block_size;

    alloc_1D<des_config_t>(1, des_config);

    des_config->block_size = block_size;
    des_config->block_count = block_count;
    des_config->granularity = 1;

    // in/output versions
    size_t input_size = block_count * sizeof(uint64_t);
    size_t output_size = block_count * sizeof(uint64_t);
    int count = cache_size / (input_size + output_size) + 1;

    config = (config_t *)des_config;

    return count;
}

void des_input_init(int count,
                    config_t *config,
                    input_t **&input) {

    des_config_t *des_config = (des_config_t *)config;
    des_input_t **des_input = (des_input_t **)input;

    // initializing input versions
    alloc_1D<des_input_t *>(count, des_input);

    // initializing individual versions
    for (int i = 0; i < count; i++) {
        alloc_1D<des_input_t>(1, des_input[i]);

        init_alloc_1D<uint64_t>(des_config->block_count, des_input[i]->state_in);
    }

    input = (input_t **)des_input;
}

void des_output_init(int count,
                     config_t *config,
                     output_t **&output) {

    des_config_t *des_config = (des_config_t *)config;
    des_output_t **des_output = (des_output_t **)output;

    // initializing output versions
    alloc_1D<des_output_t *>(count, des_output);

    // initializing individual versions
    for (int i = 0; i < count; i++) {
        alloc_1D<des_output_t>(1, des_output[i]);

        alloc_1D<uint64_t>(des_config->block_count, des_output[i]->state_out);
    }

    output = (output_t **)des_output;
}

void des_comparer(config_t *config,
                  output_t *output_scalar,
                  output_t *output_neon) {
    des_config_t *des_config = (des_config_t *)config;
    des_output_t *des_output_scalar = (des_output_t *)output_scalar;
    des_output_t *des_output_neon = (des_output_t *)output_neon;

    compare_1D<uint64_t>(des_config->block_count, des_output_scalar->state_out, des_output_neon->state_out, (char *)"state_out");
}

kernel_utility_t des_utility = {des_config_init, des_input_init, des_output_init, des_comparer};